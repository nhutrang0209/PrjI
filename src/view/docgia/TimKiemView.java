    private void updateTable( List<SachModel> list){
         try{
            tblModel.setRowCount(0);
            if(!list.isEmpty())
            for(SachModel s:list){
                tblModel.addRow(new Object[]{
                s.getMasach(),
                s.getTensach(),
                s.getTentacgia(),
                s.getNxb(),
                s.getTheloai(),
                s.getSoluongconlai(),
                s.getMavitri(),
                s.getGiabia()
            });
            }
            tblModel.fireTableDataChanged();
            
        }
        
        
        catch(Exception e){
            e.printStackTrace();
                JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
